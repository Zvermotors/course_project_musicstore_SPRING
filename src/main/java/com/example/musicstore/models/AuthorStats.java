package com.example.musicstore.models;

import lombok.Data;

import java.math.BigDecimal;

/**
 * Класс AuthorStats представляет статистические данные по конкретному автору/исполнителю.
 * Содержит ключевые метрики эффективности и популярности автора в музыкальном магазине.
 * Используется для анализа вклада отдельного автора в общие продажи.
 */
@Data // Аннотация Lombok для автоматической генерации геттеров, сеттеров, toString, equals и hashCode
public class AuthorStats {

    /**
     * Имя автора/исполнителя или название группы
     * Используется для идентификации и отображения в отчетах
     */
    private String author;

    /**
     * Общее количество проданных единиц товаров данного автора
     * Включает все продукты (альбомы, треки, мерч), связанные с автором
     */
    private Long salesCount;

    /**
     * Общая выручка от продажи товаров данного автора
     * Тип BigDecimal используется для точных денежных расчетов без ошибок округления
     */
    private BigDecimal totalRevenue;

    /**
     * Доля рынка автора в процентах от общей выручки магазина
     * Рассчитывается как (totalRevenue автора / общая выручка магазина) * 100
     * Позволяет оценить относительную значимость автора в бизнесе
     */
    private Double marketShare;
}
//Полное описание класса:
//
//Назначение:
//Класс AuthorStats является DTO (Data Transfer Object), предназначенным для хранения и передачи детализированной статистики по отдельному автору/исполнителю. Служит для анализа эффективности и популярности конкретного автора в контексте общего бизнеса магазина.
//
//Ключевые особенности:
//
//Комплексные метрики - сочетает абсолютные (salesCount, totalRevenue) и относительные (marketShare) показатели
//
//Финансовая точность - использование BigDecimal для денежных расчетов
//
//Сравнительный анализ - marketShare позволяет оценить значимость автора относительно других
//
//Типичное использование:
//
//Анализ эффективности отдельных авторов
//
//Формирование рейтингов популярности исполнителей
//
//Принятие решений о закупках и стоке
//
//Оценка рентабельности работы с конкретными авторами
//
//Планирование маркетинговых кампаний и промо-акций
//
//Преимущества использования:
//
//Полнота данных - содержит все ключевые метрики для анализа автора
//
//Сравнимость - marketShare позволяет сравнивать авторов между собой
//
//Точность - BigDecimal обеспечивает корректные финансовые расчеты
//
//Гибкость - может использоваться как самостоятельно, так и в составе AuthorsReport