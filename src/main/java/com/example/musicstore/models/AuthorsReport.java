package com.example.musicstore.models;

import lombok.Data;

import java.util.List;

/**
 * Класс AuthorsReport представляет отчет по авторам/исполнителям.
 * Содержит статистическую информацию о популярности и активности авторов в музыкальном магазине.
 * Используется для аналитики и отчетности по авторам товаров (альбомов, треков и т.д.).
 */
@Data // Аннотация Lombok для автоматической генерации геттеров, сеттеров, toString, equals и hashCode
public class AuthorsReport {

    /**
     * Список статистики по топовым авторам/исполнителям
     * Содержит объекты AuthorStats с детализированной информацией по каждому автору
     * Обычно отсортирован по убыванию популярности или продаж
     */
    private List<AuthorStats> topAuthors;

    /**
     * Общее количество авторов/исполнителей в системе
     * Используется для анализа охвата и разнообразия авторов в магазине
     */
    private Long totalAuthors;
}
//Полное описание класса:
//
//Назначение:
//Класс AuthorsReport является DTO (Data Transfer Object), предназначенным для формирования комплексных отчетов об авторах/исполнителях в музыкальном магазине. Служит для анализа популярности авторов и общего охвата каталога.
//
//Ключевые особенности:
//
//Топ-авторы - содержит список наиболее популярных или продаваемых авторов
//
//Общая статистика - показывает общее количество авторов в системе
//
//Агрегированные данные - объединяет детальную и общую статистику в одном объекте
//
//Типичное использование:
//
//Формирование отчетов о самых популярных исполнителях
//
//Анализ эффективности работы с разными авторами
//
//Планирование закупок и мерчандайзинга
//
//Оценка разнообразия каталога магазина
//
//Предоставление аналитики отделу маркетинга
//
//Преимущества использования:
//
//Комплексность - объединяет детальную и общую статистику
//
//Гибкость - позволяет варьировать количество топ-авторов в отчете
//
//Удобство - вся необходимая информация об авторах в одном объекте
//
//Сериализуемость - легко преобразуется в JSON для API или веб-интерфейса
//
//Структура отчета:
//
//topAuthors - обычно содержит 5-10 самых успешных авторов
//
//totalAuthors - показывает общее разнообразие каталога