// Пакет, в котором находится класс
package com.example.musicstore.models;

// Импорт аннотации @Data из проекта Lombok
import lombok.Data;
// Импорт класса BigDecimal для работы с денежными суммами
import java.math.BigDecimal;

/**
 * Класс DashboardStats представляет статистические данные для панели управления.
 * Содержит агрегированную информацию о состоянии магазина для отображения в админ-панели.
 */
@Data // Аннотация Lombok, которая автоматически генерирует:
// - геттеры и сеттеры для всех полей
// - методы toString(), equals() и hashCode()
// - конструктор без аргументов
public class DashboardStats {

    // Общее количество товаров в системе
    private Long totalProducts;

    // Количество товаров, доступных для покупки
    private Long availableProducts;

    // Количество забронированных товаров
    private Long bookedProducts;

    // Количество проданных товаров
    private Long soldProducts;

    // Общее количество заказов
    private Long totalOrders;

    // Количество заказов в статусе "ожидает обработки"
    private Long pendingOrders;

    // Количество завершенных заказов
    private Long completedOrders;

    // Общая выручка магазина (используется BigDecimal для точных денежных расчетов)
    private BigDecimal totalRevenue;

    // Общее количество продаж (может отличаться от количества заказов)
    private Long totalSales;

    // Общее количество авторов/исполнителей в каталоге
    private Long totalAuthors;

}
//Что делает класс DashboardStats:
//
//Этот класс является DTO (Data Transfer Object), который агрегирует ключевые метрики для отображения на панели управления (dashboard). Он предоставляет:
//
//Статистика по товарам:
//
//totalProducts - общее количество товаров
//
//availableProducts - товары в наличии
//
//bookedProducts - забронированные товары
//
//soldProducts - проданные товары
//
//Статистика по заказам:
//
//totalOrders - все заказы
//
//pendingOrders - заказы в обработке
//
//completedOrders - выполненные заказы
//
//Финансовая статистика:
//
//totalRevenue - общая выручка (с точностью до копеек)
//
//totalSales - общее количество продаж
//
//Статистика по контенту:
//
//totalAuthors - количество авторов/исполнителей
//
//Класс используется для:
//
//Отображения сводной информации в админ-панели
//
//Формирования отчетов о состоянии бизнеса
//
//Передачи статистических данных между сервисом и контроллером
//
//Визуализации ключевых показателей эффективности (KPI)