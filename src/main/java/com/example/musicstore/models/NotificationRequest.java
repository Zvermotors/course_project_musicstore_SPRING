// Пакет, в котором находится класс
package com.example.musicstore.models;

// Импорт аннотации @Data из проекта Lombok
import lombok.Data;

/**
 * Класс NotificationRequest представляет запрос на отправку уведомления.
 * Содержит параметры для настройки и отправки различных типов уведомлений.
 */
@Data // Аннотация Lombok, которая автоматически генерирует:
// - геттеры и сеттеры для всех полей
// - методы toString(), equals() и hashCode()
// - конструктор без аргументов
public class NotificationRequest {

    // Поле для хранения темы (заголовка) уведомления
    private String subject;

    // Поле для хранения текста сообщения уведомления
    private String message;

    // Поле для хранения типа уведомления (из перечисления NotificationType)
    private NotificationType type;

    // Поле для хранения email конкретного пользователя для целевой отправки
    private String targetEmail; // для отправки конкретному пользователю

    /**
     * Перечисление типов уведомлений, определяющее целевую аудиторию отправки
     */
    public enum NotificationType {
        ALL_USERS,        // Отправка всем пользователям системы
        SPECIFIC_USER,    // Отправка конкретному пользователю (требуется targetEmail)
        ACTIVE_USERS      // Отправка только активным пользователям
    }
}
//Что делает класс NotificationRequest:
//
//Этот класс является DTO (Data Transfer Object), который используется для передачи параметров запроса на отправку уведомлений. Он содержит:
//
//subject - заголовок или тема уведомления
//
//message - основное текстовое содержание уведомления
//
//type - тип рассылки (определяет целевую аудиторию)
//
//targetEmail - опциональное поле для указания конкретного получателя
//
//Перечисление NotificationType определяет:
//
//ALL_USERS - массовая рассылка всем пользователям
//
//SPECIFIC_USER - точечная отправка конкретному пользователю
//
//ACTIVE_USERS - рассылка только активным (недавно заходившим) пользователям
//
//Класс используется для:
//
//Обработки запросов на отправку уведомлений из API
//
//Конфигурации email-рассылок
//
//Управления целевыми аудиториями для нотификаций
//
//Передачи параметров между контроллером и сервисом уведомлений