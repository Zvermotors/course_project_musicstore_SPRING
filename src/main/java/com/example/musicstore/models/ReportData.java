// Пакет, в котором находится класс
package com.example.musicstore.models;

// Импорт аннотации @Data из проекта Lombok
import lombok.Data;

/**
 * Класс ReportData представляет комплексный отчет, содержащий различные типы данных.
 * Используется для агрегации всех видов отчетов в одном объекте для удобной передачи.
 */
@Data // Аннотация Lombok, которая автоматически генерирует:
// - геттеры и сеттеры для всех полей
// - методы toString(), equals() и hashCode()
// - конструктор без аргументов
public class ReportData {

    // Статистика панели управления (основные метрики системы)
    private DashboardStats dashboard;

    // Отчет по выручке и финансовым показателям
    private RevenueReport revenue;

    // Отчет по авторам/исполнителям
    private AuthorsReport authors;

    // Отчет по продажам и товарообороту
    private SalesReport sales;

}
//Что делает класс ReportData:
//
//Этот класс является контейнером-агрегатором для различных типов отчетов. Он выполняет следующие функции:
//
//Назначение:
//
//Объединяет различные типы отчетов в единую структуру
//
//Упрощает передачу комплексных данных между слоями приложения
//
//Позволяет получать все необходимые отчеты за один запрос
//
//Содержащиеся отчеты:
//
//dashboard - основные метрики системы (DashboardStats)
//
//revenue - финансовые показатели и выручка (RevenueReport)
//
//authors - статистика по авторам/исполнителям (AuthorsReport)
//
//sales - данные о продажах и товарообороте (SalesReport)
//
//Преимущества использования:
//
//Уменьшает количество API вызовов (все отчеты в одном ответе)
//
//Обеспечивает структурированность данных
//
//Упрощает обработку на клиентской стороне
//
//Позволяет легко расширять систему отчетности
//
//Типичные use-cases:
//
//Формирование комплексной аналитической панели
//
//Генерация сводных отчетов для руководства
//
//Экспорт данных в внешние системы
//
//Отображение полной бизнес-статистики