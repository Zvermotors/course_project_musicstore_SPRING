package com.example.musicstore.models;

import lombok.Data;

import java.math.BigDecimal;
import java.time.LocalDate;

/**
 * Класс RevenueByPeriod представляет модель данных для хранения информации о выручке за определенный период времени.
 * Используется для аналитики и отчетности по продажам в музыкальном магазине.
 * Содержит агрегированные данные о доходах, количестве продаж и среднем чеке за период.
 */
@Data // Аннотация Lombok для автоматической генерации геттеров, сеттеров, toString, equals и hashCode
public class RevenueByPeriod {

    /**
     * Период времени, за который собрана статистика (например, конкретная дата)
     * Тип LocalDate обеспечивает работу с датами без времени
     */
    private LocalDate period;

    /**
     * Общая выручка за указанный период
     * Тип BigDecimal используется для точных денежных расчетов без ошибок округления
     */
    private BigDecimal revenue;

    /**
     * Количество продаж (заказов) за указанный период
     */
    private Long salesCount;

    /**
     * Средняя стоимость заказа (средний чек) за период
     * Рассчитывается как revenue / salesCount
     * Тип BigDecimal обеспечивает точность финансовых расчетов
     */
    private BigDecimal averageOrderValue;
}


//Полное описание класса:
//
//Назначение:
//Класс RevenueByPeriod является DTO (Data Transfer Object), предназначенным для хранения и передачи агрегированных данных о финансовых показателях за определенные периоды времени. Используется преимущественно для генерации отчетов и аналитики продаж.
//
//Ключевые особенности:
//
//Периодичность данных - позволяет анализировать выручку по дням, неделям, месяцам
//
//Финансовая точность - использование BigDecimal исключает ошибки округления при денежных расчетах
//
//Комплексная аналитика - содержит не только общую выручку, но и метрики эффективности продаж
//
//Типичное использование:
//
//Генерация ежедневных/еженедельных/ежемесячных отчетов о продажах
//
//Анализ динамики выручки во времени
//
//Расчет среднего чека и эффективности продаж
//
//Построение графиков и диаграмм продаж
//
//Преимущества использования:
//
//Иммутабельность - данные отчета не изменяются после создания
//
//Сериализуемость - может легко преобразовываться в JSON для API
//
//Типобезопасность - строгая типизация всех финансовых показателей