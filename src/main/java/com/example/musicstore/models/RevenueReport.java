package com.example.musicstore.models;

import lombok.Data;

import java.math.BigDecimal;
import java.time.LocalDate;
import java.util.List;

/**
 * Класс RevenueReport представляет комплексный отчет о выручке и финансовых показателях.
 * Содержит аналитическую информацию о доходах магазина, включая периодическую разбивку,
 * средний чек и информацию о лучшем дне по выручке.
 * Используется для финансового анализа и планирования доходов.
 */
@Data // Аннотация Lombok для автоматической генерации геттеров, сеттеров, toString, equals и hashCode
public class RevenueReport {

    /**
     * Список данных о выручке по периодам (дням, неделям, месяцам)
     * Содержит объекты RevenueByPeriod с детализированной финансовой информацией
     * за каждый период, включая выручку, количество продаж и средний чек
     */
    private List<RevenueByPeriod> byPeriod;

    /**
     * Средний чек по всем заказам за отчетный период
     * Рассчитывается как общая выручка / количество заказов
     * Тип BigDecimal используется для точных денежных расчетов
     * Важный показатель эффективности продаж и ценовой политики
     */
    private BigDecimal averageCheck;

    /**
     * Дата дня с максимальной выручкой за отчетный период
     * Используется для анализа пиковых дней продаж и планирования промо-акций
     */
    private LocalDate bestDay;

    /**
     * Выручка в лучший день за отчетный период
     * Тип BigDecimal обеспечивает точность финансовых данных
     * Показывает максимальный потенциал дневных продаж
     */
    private BigDecimal bestDayRevenue;
}
