//
//Назначение:
//Класс RevenueReport является комплексным DTO (Data Transfer Object), предназначенным для формирования детальных финансовых отчетов о выручке. Сочетает периодический анализ, ключевые финансовые метрики и информацию о рекордных показателях.
//
//Ключевые особенности:
//
//Многоуровневый анализ - объединяет периодические данные и агрегированные метрики
//
//Финансовая фокусировка - специализируется на денежных показателях эффективности
//
//Бенчмаркинг - включает информацию о лучших показателях для сравнения
//
//Типичное использование:
//
//Ежемесячные финансовые отчеты для руководства
//
//Анализ эффективности ценовой политики
//
//Планирование бюджета и прогнозирование доходов
//
//Выявление сезонных пиков и спадов выручки
//
//Оценка успешности промо-акций и распродаж
//
//Преимущества использования:
//
//Комплексность - предоставляет полную финансовую картину в одном объекте
//
//Аналитическая ценность - содержит как детальные, так и суммарные метрики
//
//Сравнительный анализ - bestDay позволяет оценить потенциал продаж
//
//Точность расчетов - использование BigDecimal для финансовых данных