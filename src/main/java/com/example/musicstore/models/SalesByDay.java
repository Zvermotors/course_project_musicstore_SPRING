package com.example.musicstore.models;

import lombok.Data;

import java.math.BigDecimal;
import java.time.LocalDate;

/**
 * Класс SalesByDay представляет модель данных для хранения ежедневной статистики продаж.
 * Используется для анализа и отчетности по продажам с разбивкой по дням.
 * Содержит агрегированные данные о количестве продаж и выручке за конкретный день.
 */
@Data // Аннотация Lombok для автоматической генерации геттеров, сеттеров, toString, equals и hashCode
public class SalesByDay {

    /**
     * Конкретная дата, за которую собрана статистика продаж
     * Тип LocalDate обеспечивает работу с датами без времени
     */
    private LocalDate date;

    /**
     * Количество продаж (заказов) за указанный день
     * Используется для анализа активности продаж по дням
     */
    private Long salesCount;

    /**
     * Общая выручка за указанный день
     * Тип BigDecimal используется для точных денежных расчетов без ошибок округления
     */
    private BigDecimal revenue;
}
//Полное описание класса:
//
//Назначение:
//Класс SalesByDay является DTO (Data Transfer Object), предназначенным для хранения и передачи агрегированных данных о продажах за конкретные дни. Используется для построения ежедневной отчетности и анализа динамики продаж.
//
//Ключевые особенности:
//
//Дневная детализация - позволяет анализировать продажи с привязкой к конкретным датам
//
//Ключевые метрики - содержит как количественные (salesCount), так и финансовые (revenue) показатели
//
//Простота структуры - минимальный набор полей для эффективного анализа трендов
//
//Типичное использование:
//
//Построение графиков ежедневных продаж
//
//Анализ сезонности и дней недели
//
//Сравнение эффективности продаж по дням
//
//Выявление пиковых и спадовых дней продаж
//
//Формирование ежедневных отчетов для менеджмента
//
//Преимущества использования:
//
//Простота - легковесная структура с минимальным набором полей
//
//Удобство анализа - идеально подходит для построения временных рядов
//
//Совместимость - легко преобразуется в JSON для API или графических библиотек
//
//Типобезопасность - строгая типизация всех полей
//
//Отличие от RevenueByPeriod:
//
//SalesByDay фокусируется исключительно на daily-статистике
//
//RevenueByPeriod может представлять данные за различные периоды (дни, недели, месяцы)
//
//SalesByDay не содержит расчетных полей (как averageOrderValue)