package com.example.musicstore.models;

import lombok.Data;

import java.util.List;

/**
 * Класс SalesReport представляет комплексный отчет о продажах.
 * Содержит разнообразную аналитическую информацию о продажах в музыкальном магазине,
 * объединяя данные по дням и распределение по статусам заказов.
 * Используется для всестороннего анализа эффективности продаж.
 */
@Data // Аннотация Lombok для автоматической генерации геттеров, сеттеров, toString, equals и hashCode
public class SalesReport {

    /**
     * Список ежедневной статистики продаж
     * Содержит объекты SalesByDay с детализированной информацией по каждому дню
     * Позволяет анализировать динамику продаж во времени
     */
    private List<SalesByDay> byDay;

    /**
     * Распределение заказов по статусам
     * Содержит информацию о количестве заказов в каждом статусе (подтвержденные,
     * выполненные, отмененные и т.д.)
     * Важно для анализа эффективности процесса обработки заказов
     */
    private StatusDistribution statusDistribution;
}
//Полное описание класса:
//
//Назначение:
//Класс SalesReport является комплексным DTO (Data Transfer Object), предназначенным для формирования многоаспектных отчетов о продажах. Объединяет временной анализ (по дням) и анализ процесса обработки заказов (по статусам).
//
//Ключевые особенности:
//
//Многомерный анализ - сочетает временные и процессные метрики
//
//Комплексность - предоставляет полную картину продаж в одном объекте
//
//Гибкость структуры - может быть расширен дополнительными метриками
//
//Типичное использование:
//
//Ежедневные/еженедельные/ежемесячные отчеты о продажах
//
//Анализ эффективности работы отдела продаж
//
//Мониторинг выполнения планов продаж
//
//Выявление проблем в процессе обработки заказов
//
//Формирование отчетности для руководства
//
//Преимущества использования:
//
//Полнота данных - объединяет ключевые метрики продаж в одном отчете
//
//Удобство анализа - предоставляет данные для разных аспектов анализа
//
//Структурированность - четкое разделение на временные и процессные метрики
//
//Сериализуемость - легко преобразуется в JSON для API или веб-интерфейса