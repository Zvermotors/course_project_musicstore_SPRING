// Пакет, в котором находится класс
package com.example.musicstore.models;

// Импорт аннотации @Data из проекта Lombok
import lombok.Data;

/**
 * Класс StatusDistribution представляет распределение статусов товаров.
 * Используется для статистики и анализа количества товаров по различным статусам.
 */
@Data // Аннотация Lombok, которая автоматически генерирует:
// - геттеры и сеттеры для всех полей
// - методы toString(), equals() и hashCode()
// - конструктор без аргументов
public class StatusDistribution {

    // Поле для хранения количества доступных (available) товаров
    private Long available;

    // Поле для хранения количества забронированных (booked) товаров
    private Long booked;

    // Поле для хранения количества проданных (sold) товаров
    private Long sold;
}
//Что делает класс StatusDistribution:
//
//Этот класс является POJO (Plain Old Java Object), который служит контейнером для хранения статистической информации о распределении товаров по статусам. Он содержит три числовых поля:
//
//available - количество товаров, доступных для покупки
//
//booked - количество товаров, которые забронированы (зарезервированы)
//
//sold - количество проданных товаров
//
//Благодаря аннотации @Data из библиотеки Lombok, класс автоматически получает все необходимые методы для работы с данными (геттеры, сеттеры, toString и др.), что делает код более чистым и удобочитаемым.
//
//Этот класс может использоваться для:
//
//Отображения статистики в административной панели
//
//Формирования отчетов о продажах
//
//Анализа эффективности товарооборота
//
//Передачи данных между слоями приложения (контроллер → сервис → репозиторий)