// Объявление пакета, в котором находится перечисление
package com.example.musicstore.models.enums;

// Объявление перечисления OrderStatus, которое представляет возможные статусы заказа в системе
public enum OrderStatus {

    // Константа перечисления - статус "Ожидает обработки"
    // Заказ создан, но еще не обработан администратором
    PENDING,    // Ожидает обработки

    // Константа перечисления - статус "Подтвержден"
    // Заказ проверен и подтвержден администратором, готов к оплате и выполнению
    CONFIRMED,  // Подтвержден

    // Константа перечисления - статус "Завершен"
    // Заказ полностью выполнен: оплачен, товар доставлен или получен
    COMPLETED,  // Завершен (оплачен и доставлен)

    // Константа перечисления - статус "Отменен"
    // Заказ отменен по инициативе пользователя или администратора
    CANCELLED;  // Отменен

    // Метод для получения читаемого (отображаемого) названия статуса на русском языке
    public String getDisplayName() {
        // Использование switch expression (доступно с Java 14) для возврата соответствующего названия
        return switch (this) {
            case PENDING -> "Ожидание";         // Отображаемое название для статуса PENDING
            case CONFIRMED -> "Подтвержден";    // Отображаемое название для статуса CONFIRMED
            case COMPLETED -> "Завершен";       // Отображаемое название для статуса COMPLETED
            case CANCELLED -> "Отменен";        // Отображаемое название для статуса CANCELLED
        };
    }
}